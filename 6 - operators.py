# Basic operators in Python
print("=== PYTHON OPERATORS ===")

# Math operators
a = 10
b = 3
print(f"{a} + {b} = {a + b}")       # 13
print(f"{a} > {b} = {a > b}")       # True

# Text operators
name = "Python"
print(f"Hello " + name + "!")       # Hello Python!

